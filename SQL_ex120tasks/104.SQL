SELECT
	class, num
FROM
	Classes
INNER JOIN
	(SELECT 5*5*(a-1)+5*(b-1) + c AS n, 'bc-' + CAST(5*5*(a-1)+5*(b-1) + c AS VARCHAR(20)) AS num 
	FROM (SELECT 1 a UNION ALL SELECT 2 UNION ALL SELECT 3 
	 UNION ALL SELECT 4 UNION ALL SELECT 5
	 ) x CROSS JOIN
	 (SELECT 1 b UNION ALL SELECT 2 UNION ALL SELECT 3 
	 UNION ALL SELECT 4 UNION ALL SELECT 5
	 ) y CROSS JOIN
	 (SELECT 1 c UNION ALL SELECT 2 UNION ALL SELECT 3 
	 UNION ALL SELECT 4 UNION ALL SELECT 5
	 ) z
	WHERE 5*5*(a-1)+5*(b-1) + c <= 100) t
ON
	n <= numGuns
WHERE
	type = 'bc'
